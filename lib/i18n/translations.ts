const all: Partial<Record<string, TranslationDict>> = { en, ar, ...generatedLocales };

export function normalizeLanguageCode(code: string | null | undefined): SupportedLanguage {
  const c = (code ?? '').trim().toLowerCase();
  if (!c) return 'en';
  return SUPPORTED_LANGUAGES.includes(c) ? c : 'en';
}

export function tFor(languageCode: string, key: string): string {
  const lang = normalizeLanguageCode(languageCode);
  return all[lang]?.[key] ?? all.en?.[key] ?? key;
}

export function localeForLanguage(languageCode: string): string {
  const lang = normalizeLanguageCode(languageCode);
  if (lang === 'ur') return 'ur-PK';
  if (lang === 'ar') return 'ar-SA';
  if (lang === 'hi') return 'hi-IN';
  if (lang === 'bn') return 'bn-BD';
  if (lang === 'pa') return 'pa-IN';
  if (lang === 'gu') return 'gu-IN';
  if (lang === 'ta') return 'ta-IN';
  if (lang === 'te') return 'te-IN';
  if (lang === 'mr') return 'mr-IN';
  if (lang === 'fa') return 'fa-IR';
  if (lang === 'zh') return 'zh-CN';
  if (lang === 'ja') return 'ja-JP';
  if (lang === 'ko') return 'ko-KR';
  if (lang === 'th') return 'th-TH';
  if (lang === 'vi') return 'vi-VN';
  if (lang === 'id') return 'id-ID';
  if (lang === 'ms') return 'ms-MY';
  if (lang === 'en') return 'en-US';
  if (lang === 'es') return 'es-419';
  return lang;
}
