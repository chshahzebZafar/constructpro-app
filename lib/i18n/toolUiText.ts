export function localizeKnownUiText(t: TranslateFn, phrase: string): string {
  const exact = PHRASE_TO_KEY[phrase];
  if (exact) {
    const translated = t(exact);
    return translated === exact ? phrase : translated;
  }
  // Handle currency/unit decorated labels like "Total investment ($)"
  const currencyLabel = phrase.replace(/\s*\([^)]*\)\s*$/, '');
  const key = PHRASE_TO_KEY[currencyLabel];
  if (!key) return phrase;
  const translated = t(key);
  return translated === key ? phrase : translated;
}

